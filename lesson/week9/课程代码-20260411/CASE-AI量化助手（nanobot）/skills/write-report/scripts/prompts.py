#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
国泰君安"五步法"Prompt 模板

每一步都有专属的 Prompt，用于引导 LLM 从财报 RAG 数据中进行结构化分析。
五步法：信息差 -> 逻辑差 -> 预期差 -> 催化剂 -> 结论
"""


# Step 1: 信息差 - 发现市场忽视的关键信息
INFORMATION_GAP_PROMPT = """你是一位资深投资分析师，正在分析{stock_name}的财报数据。

任务：寻找"信息差"——市场尚未充分关注或被忽视的关键信息。

请基于以下从财报中检索到的数据，完成以下分析：

1. 【隐藏亮点】：找出财报中被市场忽视的正面信息
   - 附注中的重要变化（如关联交易、会计政策变更）
   - 非经常性损益中的规律性收入
   - 新业务/新产品的增长信号

2. 【隐藏风险】：找出财报中被市场忽视的负面信息
   - 应收账款/存货的异常变化
   - 现金流与利润的背离
   - 资产减值的潜在风险

3. 【关键数据】：提取最能体现信息差的 3-5 个核心数据点

财报相关数据：
{context}

请用结构化格式输出分析结果，每个要点附上具体数据支撑。"""


# Step 2: 逻辑差 - 纠正市场的错误推理
LOGIC_GAP_PROMPT = """你是一位资深投资分析师，正在分析{stock_name}。

基于上一步发现的信息差：
{previous_analysis}

任务：寻找"逻辑差"——市场对数据的解读可能存在哪些推理错误。

请完成以下分析：

1. 【常见误读】：市场对该公司最常见的错误认知是什么？
   - 例如：将一次性减值理解为持续性亏损
   - 例如：忽视毛利率下降的真实原因（主动降价抢份额 vs 竞争力下降）

2. 【因果重构】：基于财报数据，重新构建正确的因果逻辑链
   - 数据A -> 导致B -> 实际影响是C（而非市场认为的D）

3. 【预判修正】：市场基于错误逻辑可能做出的错误预判是什么？正确的预判应该是什么？

补充财报数据：
{context}

请用清晰的逻辑链条呈现分析，标注每个推理步骤的数据依据。"""


# Step 3: 预期差 - 量化市场预期与实际的偏离
EXPECTATION_GAP_PROMPT = """你是一位资深投资分析师，正在分析{stock_name}。

基于前两步的分析：
{previous_analysis}

任务：寻找"预期差"——市场一致预期与实际/合理预期之间的偏离。

请完成以下分析：

1. 【核心指标对比】：
   | 指标 | 市场一致预期 | 财报实际/合理估计 | 偏离幅度 |
   |------|------------|----------------|---------|
   - 营收增速
   - 净利润增速
   - 毛利率
   - ROE
   - 其他关键指标

2. 【预期差的驱动因素】：
   - 哪些因素导致了预期差？
   - 这些因素是一次性的还是可持续的？

3. 【预期差的方向】：
   - 正向预期差（实际好于预期）：具体哪些方面？
   - 负向预期差（实际差于预期）：具体哪些方面？
   - 净预期差的方向和强度判断

补充财报数据：
{context}

请尽量给出定量分析，用数据说话。如果缺少市场一致预期数据，请基于行业经验给出合理估计并注明。"""


# Step 4: 催化剂 - 识别触发价值重估的事件
CATALYST_PROMPT = """你是一位资深投资分析师，正在分析{stock_name}。

基于前三步的分析：
{previous_analysis}

任务：识别"催化剂"——可能触发市场重新评估该公司价值的事件或因素。

请完成以下分析：

1. 【短期催化剂】（1-3个月）：
   - 即将发布的财报/业绩预告
   - 重大合同/订单公告
   - 行业政策变化
   - 股东大会/增持/回购计划

2. 【中期催化剂】（3-12个月）：
   - 新产品/新业务放量
   - 行业周期拐点
   - 并购/资产重组
   - 机构调研/评级变化

3. 【潜在负面催化剂】：
   - 可能引发股价下跌的风险事件

4. 【催化剂时间表】：按时间线排列最可能发生的催化剂

补充财报数据（关注公告和未来展望部分）：
{context}

请给出具体的事件描述和预计发生时间窗口。"""


# Step 5: 结论 - 综合投资建议
CONCLUSION_PROMPT = """你是一位资深投资分析师，正在为{stock_name}撰写投资结论。

前四步分析结果：
{previous_analysis}

任务：综合前四步分析，给出最终的投资结论。

请完成以下内容：

1. 【核心观点】（一句话总结）：

2. 【投资逻辑】（3-5个要点）：
   - 基于信息差的核心发现
   - 基于逻辑差的推理修正
   - 基于预期差的方向判断
   - 基于催化剂的时间窗口

3. 【投资评级】：
   - 评级：强烈推荐 / 推荐 / 中性 / 回避
   - 评级依据

4. 【关键假设与风险】：
   - 上述结论成立的关键假设是什么？
   - 如果假设被证伪，最大的风险是什么？

5. 【关注指标】：
   - 投资者应持续跟踪的 3-5 个关键指标

补充财报数据：
{context}

请注意：
- 投资建议仅供参考，不构成投资决策依据
- 需要附上免责声明"""


